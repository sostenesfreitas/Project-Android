
package com.example.sample.antriksh.retrofitrxandroidexample.api;

public class Pokemon {

    private String name;
    private String basicAtk;
    private String chargeAtk;
    private String chargeDamage;
    private String rankOff;
    private String rankDef;
    private String tank;
    private String gymOff;
    private String gymDef;
    private String damage;
    private String gynDamage;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBasicAtk() {
        return basicAtk;
    }

    public void setBasicAtk(String basicAtk) {
        this.basicAtk = basicAtk;
    }

    public String getChargeAtk() {
        return chargeAtk;
    }

    public void setChargeAtk(String chargeAtk) {
        this.chargeAtk = chargeAtk;
    }

    public String getChargeDamage() {
        return chargeDamage;
    }

    public void setChargeDamage(String chargeDamage) {
        this.chargeDamage = chargeDamage;
    }

    public String getRankOff() {
        return rankOff;
    }

    public void setRankOff(String rankOff) {
        this.rankOff = rankOff;
    }

    public String getRankDef() {
        return rankDef;
    }

    public void setRankDef(String rankDef) {
        this.rankDef = rankDef;
    }

    public String getTank() {
        return tank;
    }

    public void setTank(String tank) {
        this.tank = tank;
    }

    public String getGymOff() {
        return gymOff;
    }

    public void setGymOff(String gymOff) {
        this.gymOff = gymOff;
    }

    public String getGymDef() {
        return gymDef;
    }

    public void setGymDef(String gymDef) {
        this.gymDef = gymDef;
    }

    public String getDamage() {
        return damage;
    }

    public void setDamage(String damage) {
        this.damage = damage;
    }

    public String getGynDamage() {
        return gynDamage;
    }

    public void setGynDamage(String gynDamage) {
        this.gynDamage = gynDamage;
    }
}
